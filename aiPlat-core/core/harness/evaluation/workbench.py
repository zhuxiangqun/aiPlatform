"""
Evaluator Workbench (P1)

Goal:
- Standardize evaluator report schema (JSON)
- Apply threshold gate (pass/fail)
- Persist as LearningArtifact + run_events for observability

This module is intentionally LLM-agnostic: it accepts a report JSON payload
generated by any evaluator agent (human/LLM/Playwright).
"""

from __future__ import annotations

import time
from dataclasses import dataclass, field
from typing import Any, Dict, Optional, Tuple

from core.learning.manager import LearningManager
from core.learning.types import LearningArtifactKind


@dataclass
class EvaluatorThresholds:
    functionality_min: float = 7.0

    @classmethod
    def from_dict(cls, d: Optional[Dict[str, Any]]) -> "EvaluatorThresholds":
        dd = d or {}
        try:
            fm = float(dd.get("functionality_min", cls.functionality_min))
        except Exception:
            fm = cls.functionality_min
        return cls(functionality_min=fm)

    def to_dict(self) -> Dict[str, Any]:
        return {"functionality_min": self.functionality_min}


def _get_score(report: Dict[str, Any], key: str) -> float:
    score = (report.get("score") or {}) if isinstance(report.get("score"), dict) else {}
    v = score.get(key)
    try:
        return float(v)
    except Exception:
        return 0.0


def validate_report(report: Dict[str, Any]) -> Tuple[bool, str]:
    """
    Minimal schema validation (fail-fast but tolerant).
    """
    if not isinstance(report, dict):
        return False, "report_not_object"
    if "pass" not in report:
        return False, "missing_pass"
    if not isinstance(report.get("pass"), bool):
        return False, "pass_not_bool"
    # score is optional but recommended
    if "score" in report and not isinstance(report.get("score"), dict):
        return False, "score_not_object"
    if "issues" in report and not isinstance(report.get("issues"), list):
        return False, "issues_not_list"
    return True, "ok"


def apply_threshold_gate(report: Dict[str, Any], thresholds: EvaluatorThresholds) -> Dict[str, Any]:
    """
    Mutates a shallow copy of report:
    - Ensures thresholds field
    - Enforces hard threshold(s)
    - Computes overall if missing (best-effort)
    """
    r = dict(report or {})
    r.setdefault("thresholds", thresholds.to_dict())

    # Hard threshold: functionality
    functionality = _get_score(r, "functionality")
    gated_fail = functionality < thresholds.functionality_min

    # If report already says pass=false, keep it false; otherwise apply gate.
    if r.get("pass") is True and gated_fail:
        r["pass"] = False
        r.setdefault("issues", [])
        try:
            issues = r.get("issues")
            if isinstance(issues, list):
                issues.insert(
                    0,
                    {
                        "severity": "P0",
                        "title": "未达功能阈值（Functionality）",
                        "expected": f"functionality >= {thresholds.functionality_min}",
                        "actual": f"functionality = {functionality}",
                        "repro_steps": [],
                        "suggested_fix": "补齐关键路径可用性与交互深度后重新评估。",
                    },
                )
        except Exception:
            pass

    # Best-effort overall score
    score = r.get("score") if isinstance(r.get("score"), dict) else {}
    if isinstance(score, dict) and "overall" not in score:
        try:
            # average of known dims (ignore missing)
            keys = ["functionality", "product_depth", "design_ux", "code_architecture"]
            vals = []
            for k in keys:
                v = _get_score(r, k)
                if v > 0:
                    vals.append(v)
            score["overall"] = round(sum(vals) / len(vals), 2) if vals else 0.0
            r["score"] = score
        except Exception:
            pass

    r.setdefault("evaluated_at", time.time())
    return r


async def persist_evaluation(
    *,
    execution_store: Any,
    run_id: str,
    trace_id: Optional[str],
    evaluator: str,
    report: Dict[str, Any],
    thresholds: EvaluatorThresholds,
    actor: Optional[Dict[str, Any]] = None,
) -> Dict[str, Any]:
    """
    Persist report as:
    - LearningArtifact(kind=evaluation_report, target_type=run)
    - run_event(type=evaluation_report)
    - audit_log(action=run_evaluation_submit)
    """
    mgr = LearningManager(execution_store=execution_store)
    version = f"eval:{int(time.time())}"
    art = await mgr.create_artifact(
        kind=LearningArtifactKind.EVALUATION_REPORT,
        target_type="run",
        target_id=str(run_id),
        version=version,
        status="draft",
        payload=report,
        metadata={"evaluator": evaluator, "thresholds": thresholds.to_dict(), "pass": bool(report.get("pass"))},
        trace_id=str(trace_id) if trace_id else None,
        run_id=str(run_id),
    )
    # run event (best-effort)
    try:
        await execution_store.append_run_event(
            run_id=str(run_id),
            event_type="evaluation_report",
            trace_id=str(trace_id) if trace_id else None,
            tenant_id=(actor or {}).get("tenant_id") if isinstance(actor, dict) else None,
            payload={"artifact_id": art.artifact_id, "pass": bool(report.get("pass")), "evaluator": evaluator, "version": version},
        )
    except Exception:
        pass
    # audit log (best-effort)
    try:
        if hasattr(execution_store, "add_audit_log") and isinstance(actor, dict):
            await execution_store.add_audit_log(
                action="run_evaluation_submit",
                status="ok",
                tenant_id=str(actor.get("tenant_id") or "") or None,
                actor_id=str(actor.get("actor_id") or "") or None,
                actor_role=str(actor.get("actor_role") or "") or None,
                resource_type="run",
                resource_id=str(run_id),
                detail={"artifact_id": art.artifact_id, "evaluator": evaluator, "pass": bool(report.get("pass"))},
            )
    except Exception:
        pass
    return {"artifact_id": art.artifact_id, "version": version}

